package edu.iest.linearlayoutactivity

class Aritmetica {

    fun suma(numUno: Float, numDos: Float) : Float{
        return numUno + numDos
    }

    fun resta(numUno: Float, numDos: Float) : Float{
        return numUno - numDos
    }

    fun multiplicacion(numUno: Float, numDos: Float) : Float{
        return numUno * numDos
    }

    fun division(numUno: Float, numDos: Float) : Float{
        return numUno / numDos
    }




}